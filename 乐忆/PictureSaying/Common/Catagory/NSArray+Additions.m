#import "NSArray+Additions.h"

@implementation NSArray (Additions)

//数组过界返回nil
- (id)PSobjectAtIndex:(NSUInteger)index
{
    if (index < [self count])
    {
        return [self objectAtIndex:index];
    }
    else
    {
        return nil;
    }
    
}

@end
